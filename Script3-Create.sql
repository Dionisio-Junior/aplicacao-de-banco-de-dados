CREATE TABLE novela (
    id_novela INT AUTO_INCREMENT PRIMARY KEY,
    nome_novela VARCHAR(100) NOT NULL,
    ano_lancamento YEAR,
    genero VARCHAR(50),
    audiencia_media DECIMAL(10,2)
);

